#variaveis
a = 2
b = 4
c = 6

#operadores de comparação

print(a == b) # verifica se 'a' igual a 'b'
print(a != b) # verifica se 'b' é diferente de 'b'
print(b > a) # verifica se 'b' é maior que 'a'
print(b < a) # verifica se 'b' é menor que 'a'
print(b >= c) # verifica se 'b' é maior ou igual a 'c'
print(b <= c) # verifica se 'b' é menor ou igual a 'c'
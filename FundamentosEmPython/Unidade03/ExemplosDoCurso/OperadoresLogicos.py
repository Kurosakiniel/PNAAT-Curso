#variaveis
a = 2
b = 4
c = 6

#operadores lógicos
print(a == b and b < c) # 'E' Logico: Retorna True se a == b E b < c
print(a != b and b < c) # 'E' Logico: Retorna True se a != b E b < c
print(b > a and b >= c) # 'OU' Logico: Retorna True se b > a OU b >= c
print(b < a or c > b) # 'OU' Logico: Retorna True se b < a OU c > b
print(not (a == c))  # 'NÃO' Logico: Inverte o resultado da comparação
print(not (b <= a or b > c)) # 'NÃO' Logico: Inverte o resultado da comparação
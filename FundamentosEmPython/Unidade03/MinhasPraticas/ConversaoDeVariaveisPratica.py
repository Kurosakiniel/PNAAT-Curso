# Conversão de string para inteiro
numero_str = "505"
numero_int = int(numero_str) 
print(numero_int) # Saída: 505

# Conversão de string para float
numero_str = "200.52"
numero_float = float(numero_str) 
print(numero_float) # Saída: 200.52

# Conversão de inteiro para string
numero_int = 1000
numero_str = str(numero_int) 
print(numero_str) # Saída: "1000"

# Conversão de float para string
numero_float = 16.5
numero_str = str(numero_float) 
print(numero_str) # Saída: "16.5"

# Conversão de inteiro para float
numero_int = 20
numero_float = float(numero_int) 
print(numero_float) # Saída: 20.0"

# Conversão de float para inteiro
numero_float = 55.5
numero_int = int(numero_float)
print(numero_int) # Saída: 55

# Conversão de string para booleano
boolean_str = "True"
boolean_val = bool(boolean_str) 
print(boolean_val) # Saída: True

# Conversão de inteiro para booleano
numero_int = 1
boolean_val = bool(numero_int) 
print(boolean_val) # Saída: True

# Conversão de float para booleano
numero_float = 0.0
boolean_val = bool(numero_float) 
print(boolean_val) # Saída: False

# Conversão de booleano para string
boolean_val = True
boolean_str = str(boolean_val) 
print(boolean_str) # Saída: "True"

# Conversão de booleano para string
boolean_val = False
boolean_str = str(boolean_val) 
print(boolean_str) # Saída: "False"